      const response = await axios.post(
        'http://localhost:5000/api/music',
        {
          "is_auto": 1,
          "prompt": prompt,
          "lyrics": "Enter the lyrics you've created",
          "title": "Happy Songs",
          "instrumental": 0
        }
      );

